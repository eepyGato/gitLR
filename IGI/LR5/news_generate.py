def generate_news():
    print("=" * 60)
    print("ГЕНЕРАЦИЯ НОВОСТЕЙ")
    print("=" * 60)
    
    created_count = 0
    for news in news_data:
        obj, created = News.objects.get_or_create(
            title=news["title"],
            defaults={
                "short_description": news["short_description"],
                "content": news["content"],
                "is_published": True
            }
        )
        if created:
            print(f"✅ Создана новость: {news['title'][:50]}...")
            created_count += 1
        else:
            print(f"⚠️ Уже существует: {news['title'][:50]}...")
    
    print("\n" + "=" * 60)
    print(f"📊 Всего новостей в базе: {News.objects.count()}")
    print(f"✨ Создано новых: {created_count}")
    print("=" * 60)

if __name__ == "__main__":
    generate_news()